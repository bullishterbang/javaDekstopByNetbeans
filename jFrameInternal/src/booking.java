    public void input(){
        DefaultTableModel model = (DefaultTableModel)tabel.getModel();
    
        String a = null;
        if (laki.isSelected()){
            a = laki.getText();
        }else
            a = perempuan.getText();
        
    
        
        if (nama.getText().equals("")){
                   JOptionPane.showMessageDialog(null,"Data nama tidak boleh kosong !","Peringatan",JOptionPane.WARNING_MESSAGE);
               }else if(alamat.getText().equals("")){
                   JOptionPane.showMessageDialog(null,"Data alamat tidak boleh kosong !","Peringatan",JOptionPane.WARNING_MESSAGE);
               }else if(telepon.getText().equals("")){
                   JOptionPane.showMessageDialog(null,"Data alamat tidak boleh kosong !","Peringatan",JOptionPane.WARNING_MESSAGE);
               }else if(pekerjaan.getText().equals("")){
                   JOptionPane.showMessageDialog(null,"Data pekerjaan tidak boleh kosong !","Peringatan",JOptionPane.WARNING_MESSAGE);
               }else if(paket.getSelectedItem().equals("")){
                   JOptionPane.showMessageDialog(null,"Data paket tidak boleh kosong !","Peringatan",JOptionPane.WARNING_MESSAGE);
               }else if(!laki.isSelected() && !perempuan.isSelected()){
                   JOptionPane.showMessageDialog(null,"Data pinjaman tidak boleh kosong !","Peringatan",JOptionPane.WARNING_MESSAGE);
               }else{
                   JOptionPane.showMessageDialog(null,
                        "Nama: "+nama.getText()
                       +"\nAlamat: "+alamat.getText()
                       +"\nPekerjaan: "+pekerjaan.getText()
                       +"\nJenis Kelamin : "+a
                       +"\nPaket: "+paket.getSelectedItem()
                       +"\nTelepon :" +telepon.getText()
                       );
                   
                   model.addRow
            (new Object[]
            {
                nama.getText(),
                alamat.getText(),
                pekerjaan.getText(),
                a,
                paket.getSelectedItem(),
                telepon.getText()
            }
        );
               }
        
    }
            

