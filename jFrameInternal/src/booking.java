    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        DefaultTableModel model = (DefaultTableModel)tabel.getModel();
        
        nama.setText(model.getValueAt(tabel.getSelectedRow(),0).toString());
        alamat.setText(model.getValueAt(tabel.getSelectedRow(),1).toString());
        pekerjaan.setText(model.getValueAt(tabel.getSelectedRow(),2).toString());
        paket.setSelectedItem(model.getValueAt(tabel.getSelectedRow(),3).toString());
        if(model.getValueAt(tabel.getSelectedRow(),4) == laki.getText())
        {
            laki.setSelected(true);
        }else{
            perempuan.setSelected(true);
        }
        telepon.setText(model.getValueAt(tabel.getSelectedRow(),5).toString());
    }//GEN-LAST:event_tabelMouseClicked

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        DefaultTableModel model = (DefaultTableModel)tabel.getModel();
      
        model.setValueAt(nama.getText(), tabel.getSelectedRow(), 0);
        model.setValueAt(alamat.getText(), tabel.getSelectedRow(), 1);
        model.setValueAt(pekerjaan.getText(), tabel.getSelectedRow(), 2);
        model.setValueAt(paket.getSelectedItem(), tabel.getSelectedRow(), 3);
        if(laki.isSelected()){
         model.setValueAt(laki.getText(), tabel.getSelectedRow(), 4);
        }else{
            model.setValueAt(perempuan.getText(), tabel.getSelectedRow(), 4);
        }
        model.setValueAt(telepon.getText(), tabel.getSelectedRow(), 5);
    }//GEN-LAST:event_updateActionPerformed

